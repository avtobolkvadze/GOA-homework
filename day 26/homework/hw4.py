text = "Hello World"

# lower() — სტრიქონის ყველა ასო გადაიყვანება პატარა ასოდ
lower_text = text.lower()
# აქ lower_text იქნება "hello world"

# upper() — სტრიქონის ყველა ასო გადაიყვანება დიდი ასოდ
upper_text = text.upper()
# აქ upper_text იქნება "HELLO WORLD"

# capitalize() — სტრიქონის პირველი ასო გადაიყვანება დიდ ასოდ, დანარჩენი ყველა პატარა ასოდ
capitalized_text = text.capitalize()
# აქ capitalized_text იქნება "Hello world"